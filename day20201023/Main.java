public class Main {
}

class ListNode {
    int val;
    ListNode next;

    ListNode(int x) {
        val = x;
    }
}

class Solution {
    public boolean isPalindrome(ListNode head) {
        List<Integer> list = new ArrayList<>();
        while (head != null) {
            list.add(head.val);
            head = head.next;
        }
        int i = 0, j = list.size() - 1;
        while (j > i) {
            if (list.get(i).equals(list.get(j))) {
                i++;
                j--;
                continue;
            }
            return false;
        }
        return true;
    }
}
